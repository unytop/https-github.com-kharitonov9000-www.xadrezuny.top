$(function(){

var animacao = "animated flip";
var finanimacao = "webkitAnimationEnd mozAnimationEnd MSAnimationEnd oanimationend animationend";

	$(".acao").hover(function(e) {

	$(".recebe").addClass(animacao).one(finanimacao, function(){

		$(this).removeClass(animacao);

		});

		e.preventDefault();
   });

});

